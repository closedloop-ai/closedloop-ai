// @ts-check
/**
 * FEA-1791 / PLN-886 Phase 2 — emit the embedded migrations manifest.
 *
 * Reads migration.sql files from prisma/migrations in migration order and
 * writes src/main/database/migrations-manifest.ts: an ordered array of { name,
 * checksum, sql }. New migrations use 000N_<name>; the one timestamp-prefixed
 * FEA-2030 migration is kept immutable and ordered at its original slot. The
 * runtime migration runner consumes this instead of reading the filesystem, so
 * migrations survive tarball staging into the packaged asar (mirrors the
 * build-info.ts prebuild + gitignore precedent).
 *
 * Regenerated by `pnpm prebuild` ahead of build / typecheck / test. The output
 * is gitignored. Checksums are sha256 of the raw migration.sql bytes; the
 * runner compares them against the recorded checksum to detect drift.
 */
import { createHash } from "node:crypto";
import {
  existsSync,
  lstatSync,
  readdirSync,
  readFileSync,
  realpathSync,
  writeFileSync,
} from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { compareMigrationDirNames } from "./migration-order.mjs";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const appDir = path.join(scriptDir, "..");
const migrationsDir = path.join(appDir, "prisma", "migrations");
const outFile = path.join(
  appDir,
  "src",
  "main",
  "database",
  "migrations-manifest.ts"
);

const migrationDirs = readdirSync(migrationsDir, { withFileTypes: true })
  .filter((entry) => entry.isDirectory())
  .map((entry) => entry.name)
  .sort(compareMigrationDirNames);

if (migrationDirs.length === 0) {
  throw new Error(
    `generate-migrations-manifest: no migrations found in ${migrationsDir}`
  );
}

const migrationsRoot = realpathSync(migrationsDir);

const entries = migrationDirs.map((name) => {
  const sqlPath = path.join(migrationsDir, name, "migration.sql");
  // Reject symlinked migration files and any file whose realpath escapes the
  // migrations directory — the embedded SQL runs at app boot, so it must come
  // only from a regular file checked into prisma/migrations.
  const stat = lstatSync(sqlPath);
  if (!stat.isFile() || stat.isSymbolicLink()) {
    throw new Error(
      `generate-migrations-manifest: ${path.relative(appDir, sqlPath)} must be a regular, non-symlink file`
    );
  }
  const realSqlPath = realpathSync(sqlPath);
  const relativeToRoot = path.relative(migrationsRoot, realSqlPath);
  if (relativeToRoot.startsWith("..") || path.isAbsolute(relativeToRoot)) {
    throw new Error(
      `generate-migrations-manifest: ${path.relative(appDir, sqlPath)} resolves outside ${path.relative(appDir, migrationsDir)}`
    );
  }
  const sql = readFileSync(realSqlPath, "utf8");
  const checksum = createHash("sha256").update(sql, "utf8").digest("hex");
  return { name, checksum, sql };
});

const body = entries
  .map(
    (entry) =>
      `  {\n    name: ${JSON.stringify(entry.name)},\n    checksum: ${JSON.stringify(
        entry.checksum
      )},\n    sql: ${JSON.stringify(entry.sql)},\n  },`
  )
  .join("\n");

const contents = `// AUTO-GENERATED by scripts/generate-migrations-manifest.mjs — do not edit.
// FEA-1791 / PLN-886 Phase 2: ordered embedded migrations for the desktop
// SQLite runtime migration runner. Regenerated by \`pnpm prebuild\`.
import type { EmbeddedMigration } from "./migration-runner.js";

export const MIGRATIONS: readonly EmbeddedMigration[] = [
${body}
];
`;

if (existsSync(outFile) && readFileSync(outFile, "utf8") === contents) {
  process.stdout.write(
    `generate-migrations-manifest: unchanged (${entries.length} migration(s))\n`
  );
} else {
  writeFileSync(outFile, contents, "utf8");
  process.stdout.write(
    `generate-migrations-manifest: wrote ${entries.length} migration(s) to ${path.relative(appDir, outFile)}\n`
  );
}
